// Group cities by country
function groupCitiesByCountry(cities: CityData[]): Record<string, CityData[]> {
  return cities.reduce((acc, city) => {
    const key = city.country;
    if (!acc[key]) acc[key] = [];
    acc[key].push(city);
    return acc;
  }, {} as Record<string, CityData[]>);
}

